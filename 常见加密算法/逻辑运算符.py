print(int('1101', 2))

print(2>>1) #有符号右移动，负数补1，正数补0  0000 0000 0000 0010 >> 0 000 0000 0000 0001 若超出2^32,高位丢失
print(2<<1) #无符号逻辑左移，0000 0000 0000 0010 << 1 0000 0000 0000 0100 预期结果为4


